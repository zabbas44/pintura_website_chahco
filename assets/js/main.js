function applyTranslations() {
  document.documentElement.lang = currentLang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = getTranslation(key);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    element.setAttribute("placeholder", getTranslation(key));
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    element.setAttribute("alt", getTranslation(key));
  });

  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const key = element.dataset.i18nTitle;
    element.setAttribute("title", getTranslation(key));
  });

  const titleNode = document.querySelector("[data-meta-title]");
  if (titleNode) {
    document.title = getTranslation(titleNode.dataset.metaTitle);
  }

  const descriptionNode = document.querySelector('meta[name="description"][data-meta-description]');
  if (descriptionNode) {
    descriptionNode.setAttribute("content", getTranslation(descriptionNode.dataset.metaDescription));
  }

  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    button.textContent = getTranslation("nav.lang");
  });

  const menuButton = document.querySelector("[data-menu-button]");
  if (menuButton) {
    menuButton.setAttribute("aria-label", getTranslation("nav.openMenu"));
  }
}

function setLanguage(lang) {
  currentLang = lang === "es" ? "es" : "en";
  localStorage.setItem("amjadpintura.lang", currentLang);
  applyTranslations();
}

function setupLanguage() {
  const savedLang = localStorage.getItem("amjadpintura.lang");
  currentLang = savedLang === "es" ? "es" : "en";

  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    button.addEventListener("click", () => {
      setLanguage(currentLang === "en" ? "es" : "en");
    });
  });

  applyTranslations();
}

function setupMobileMenu() {
  const button = document.querySelector("[data-menu-button]");
  const menu = document.querySelector("[data-mobile-menu]");

  if (!button || !menu) return;

  button.addEventListener("click", () => {
    menu.classList.toggle("hidden");
  });
}

function setupReveal() {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("in-view");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16 }
  );

  document.querySelectorAll(".reveal").forEach((element) => observer.observe(element));
}

function setupParallax() {
  const parallaxItems = document.querySelectorAll("[data-parallax]");
  if (!parallaxItems.length) return;

  const updateParallax = () => {
    const y = window.scrollY * 0.06;
    parallaxItems.forEach((item, index) => {
      const direction = index % 2 === 0 ? 1 : -1;
      item.style.setProperty("--parallax-shift", `${direction * y}px`);
    });
  };

  updateParallax();
  window.addEventListener("scroll", updateParallax, { passive: true });
}

function normalizeContactStatus(message) {
  const map = {
    "Your request was captured in local mode. Configure SMTP variables to send real emails.":
      "contact.submit.successLocal",
    "Your request has been sent. We will contact you shortly.": "contact.submit.successSmtp",
    "Please complete all contact form fields.": "contact.submit.errorFields",
    "Unable to send your request right now. Please try again later.": "contact.submit.errorGeneric"
  };

  return getTranslation(map[message] || "contact.submit.errorGeneric");
}

function setupContactForm() {
  const form = document.querySelector("[data-contact-form]");
  if (!form) return;

  const status = document.querySelector("[data-form-status]");

  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    status.textContent = getTranslation("form.sending");

    const formData = new FormData(form);
    const payload = Object.fromEntries(formData.entries());

    try {
      const response = await fetch("/api/contact", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(payload)
      });

      const data = await response.json();

      if (!response.ok) {
        throw new Error(data.message || getTranslation("contact.submit.errorGeneric"));
      }

      status.textContent = normalizeContactStatus(data.message);
      form.reset();
    } catch (error) {
      status.textContent = normalizeContactStatus(error.message);
    }
  });
}

