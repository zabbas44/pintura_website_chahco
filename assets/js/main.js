function setupReveal() {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("in-view");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16 }
  );

  document.querySelectorAll(".reveal").forEach((element) => observer.observe(element));
}

function normalizeContactStatus(message) {
  const map = {
    "Your request was captured in local mode. Configure SMTP variables to send real emails.":
      "contact.submit.successLocal",
    "Your request has been sent. We will contact you shortly.": "contact.submit.successSmtp",
    "Please complete all contact form fields.": "contact.submit.errorFields",
    "Unable to send your request right now. Please try again later.": "contact.submit.errorGeneric"
  };

  return getTranslation(map[message] || "contact.submit.errorGeneric");
}

function setupContactForm() {
  document.querySelectorAll("[data-contact-form]").forEach((form) => {
    const status = form.querySelector("[data-form-status]") || form.parentElement?.querySelector("[data-form-status]");
    if (!status) return;

    form.addEventListener("submit", async (event) => {
      event.preventDefault();
      status.textContent = getTranslation("form.sending");

      const formData = new FormData(form);
      const payload = Object.fromEntries(formData.entries());

      try {
        const response = await fetch("/api/contact", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify(payload)
        });

        const data = await response.json();

        if (!response.ok) {
          throw new Error(data.message || getTranslation("contact.submit.errorGeneric"));
        }

        status.textContent = normalizeContactStatus(data.message);
        form.reset();
      } catch (error) {
        status.textContent = normalizeContactStatus(error.message);
      }
    });
  });
}

function setupSliders() {
  document.querySelectorAll("[data-slider]").forEach((slider) => {
    const track = slider.querySelector("[data-slider-track]");
    const prevButton = slider.querySelector("[data-slider-prev]");
    const nextButton = slider.querySelector("[data-slider-next]");
    const dotsRoot = slider.querySelector("[data-slider-dots]");

    if (!track || !prevButton || !nextButton || !dotsRoot) return;

    const getCards = () => Array.from(track.children);
    const getGroupSize = () => {
      if (slider.dataset.sliderSingle === "true") return 1;
      if (window.innerWidth < 768) return 1;
      if (window.innerWidth < 1200) return 2;
      return Number.parseInt(slider.dataset.sliderGroup || "1", 10);
    };

    const getStep = () => {
      const firstCard = getCards()[0];
      if (!firstCard) return track.clientWidth;
      const style = window.getComputedStyle(track);
      const gap = Number.parseFloat(style.columnGap || style.gap || "0");
      return firstCard.getBoundingClientRect().width + gap;
    };

    const getPageWidth = () => getStep() * getGroupSize();

    const getPageCount = () => Math.max(1, Math.ceil(getCards().length / getGroupSize()));

    const buildDots = () => {
      dotsRoot.innerHTML = "";
      Array.from({ length: getPageCount() }).forEach((_, index) => {
        const dot = document.createElement("button");
        dot.type = "button";
        dot.className = "slider-dot";
        dot.setAttribute("aria-label", `Go to slide group ${index + 1}`);
        dot.addEventListener("click", () => {
          track.scrollTo({ left: getPageWidth() * index, behavior: "smooth" });
        });
        dotsRoot.appendChild(dot);
      });
    };

    const updateDots = () => {
      const pageWidth = getPageWidth();
      const active = Math.round(track.scrollLeft / pageWidth);
      Array.from(dotsRoot.children).forEach((dot, index) => {
        dot.classList.toggle("is-active", index === active);
      });
    };

    prevButton.addEventListener("click", () => {
      track.scrollBy({ left: -getPageWidth(), behavior: "smooth" });
    });

    nextButton.addEventListener("click", () => {
      track.scrollBy({ left: getPageWidth(), behavior: "smooth" });
    });

    track.addEventListener("scroll", () => {
      window.requestAnimationFrame(updateDots);
    });

    window.addEventListener("resize", () => {
      buildDots();
      updateDots();
    });

    buildDots();
    updateDots();
  });
}

function setupServiceTabs() {
  document.querySelectorAll("[data-service-tabs]").forEach((tabsRoot) => {
    const buttons = Array.from(tabsRoot.querySelectorAll("[data-service-tab]"));
    const panels = Array.from(tabsRoot.querySelectorAll("[data-service-panel]"));

    const activateTab = (target) => {
      buttons.forEach((button) => {
        const isActive = button.dataset.serviceTab === target;
        button.classList.toggle("is-active", isActive);
        button.setAttribute("aria-selected", String(isActive));
      });

      panels.forEach((panel) => {
        const isActive = panel.dataset.servicePanel === target;
        panel.classList.toggle("is-active", isActive);
        panel.hidden = !isActive;
      });

      window.dispatchEvent(new Event("resize"));
    };

    buttons.forEach((button) => {
      button.addEventListener("click", () => activateTab(button.dataset.serviceTab));
    });
  });
}

